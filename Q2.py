# 此程序用于第二问的数值计算

# p 已经在论文正文中计算得出

p = 2 / 17

print(f"p = {p:.6f}")

# 计算参数

tao = p
Ptr = 1 - ( 1 - tao )**2
Ps  = 2 * tao * ( 1 - tao ) / Ptr
E   = 1 / Ptr - 1
S   = 275.3 * (Ps * 0.00004358881220486742 + ( 1 - Ps ) * 2 * 0.00004358881220486742) / ( E * 0.000009 + 0.0001490605884489648 )

print(f"S = {S:.6f}")